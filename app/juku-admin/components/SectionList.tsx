export function SectionList({
  sections,
  selectedSection,
  onSelect,
  onReorder,
  onAdd,
  onDelete,
  onToggleVisibility,
}: Props) {
  const [showAddMenu, setShowAddMenu] = useState(false)

  const sensors = useSensors(
    useSensor(PointerSensor),
    useSensor(KeyboardSensor, {
      coordinateGetter: sortableKeyboardCoordinates,
    })
  )

  function handleDragEnd(event: DragEndEvent) {
    const { active, over } = event

    if (over && active.id !== over.id) {
      const oldIndex = sections.findIndex((s) => s.id === active.id)
      const newIndex = sections.findIndex((s) => s.id === over.id)
      const newSections = arrayMove(sections, oldIndex, newIndex)
      onReorder(newSections)
    }
  }

  const availableSections: SectionType[] = [
    'hero',
    'features',
    'pricing',
    'teachers',
    'results',
    'access',
    'contact',
    'schedule',
    'faq',
  ]

  return (
    <div className="bg-white rounded-xl shadow-sm p-4">
      <div className="flex items-center justify-between mb-4">
        <h2 className="font-bold text-gray-800">セクション</h2>
        <div className="relative">
          <button
            onClick={() => setShowAddMenu(!showAddMenu)}
            className="flex items-center gap-1 px-3 py-1.5 text-sm font-medium text-blue-600 bg-blue-50 rounded-lg hover:bg-blue-100 transition-colors"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 4v16m8-8H4" />
            </svg>
            追加
          </button>

          {/* 追加メニュー */}
          {showAddMenu && (
            <>
              <div
                className="fixed inset-0 z-10"
                onClick={() => setShowAddMenu(false)}
              />
              <div className="absolute right-0 mt-2 w-48 bg-white rounded-xl shadow-lg border border-gray-200 py-2 z-20">
                {availableSections.map((type) => (
                  <button
                    key={type}
                    onClick={() => {
                      onAdd(type)
                      setShowAddMenu(false)
                    }}
                    className="w-full flex items-center gap-3 px-4 py-2 text-sm text-gray-700 hover:bg-gray-50"
                  >
                    <span className="text-gray-400">
                      {sectionIcons[type]}
                    </span>
                    {sectionTypeLabels[type]}
                  </button>
                ))}
              </div>
            </>
          )}
        </div>
      </div>

      {/* セクションリスト */}
      <DndContext
        sensors={sensors}
        collisionDetection={closestCenter}
        onDragEnd={handleDragEnd}
      >
        <SortableContext
          items={sections.map((s) => s.id)}
          strategy={verticalListSortingStrategy}
        >
          <div className="space-y-2">
            {sections.map((section) => (
              <SortableItem
                key={section.id}
                section={section}
                isSelected={selectedSection?.id === section.id}
                onSelect={() => onSelect(section)}
                onDelete={() => onDelete(section.id)}
                onToggleVisibility={() => onToggleVisibility(section)}
              />
            ))}
          </div>
        </SortableContext>
      </DndContext>

      {sections.length === 0 && (
        <div className="text-center py-8 text-gray-500">
          <p className="mb-2">セクションがありません</p>
          <p className="text-sm">「追加」からセクションを追加してください</p>
        </div>
      )}
    </div>
  )
}
